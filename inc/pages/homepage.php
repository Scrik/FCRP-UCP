<li><strong>Summary about the server:</strong></li>
<ul>
Fort Carson Roleplay is an international roleplaying server which focuses on high-end and very hardcore, detailed roleplay. Our staff are very professional and the main purpose of the server is to ensure that everyone has as much entertainment whilst playing with other people. Another thing we focus on is to give as many opportunities and experiences to players as possible. With that we mean that economy, systems and everything else which comes along with both mentioned before is down to the communities ideas and views on the game.

Members of the staff team ensure that no rules are broken and that everyone is treated fairly, equally & with the respect & professionalism that they deserve. There are no exceptions and favoritism when it comes to rule breaking, everyone is treated on the same basis. Unlike any other server, we are trying to be unique by thinking of new styles and systems on a regular basis in order to ensure authenticity and an enjoyable atmosphere for every player.

We have designed the faction system, business system and even the economy according to real life prices and costs. Everything is dynamic and players are the ones who create their own style of playing here on the server. Fort Carson Roleplay depends on the players to inspire new and creative ideas for everyone to enjoy.
</ul><br />
